int	ft_str_is_alpha(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		if (str[c] > 'z')
			return (0);
		if (str[c] < 'A')
			return (0);
		if (str[c] > 'Z')
			if (str[c] < 'a')
				return (0);
		c++;
	}
	return (1);
}
